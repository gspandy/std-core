public enum ECommentType {
    PRODUCT("P", "产品"), RENT_PRODUCT("PR", "租赁产品"), TRAVEL("TR", "游记");

    ECommentType(String code, String value) {
        this.code = code;
        this.value = value;
    }

    private String code;

    private String value;

    public String getCode() {
        return code;
    }

    public String getValue() {
        return value;
    }
}
